@app.cell
def _(basis, np, sigmax, sigmaz):
    # coefficients
    delta = 2 * np.pi
    g = 0.25

    # hamiltonian
    H = delta / 2.0 * sigmax()

    # list of collapse operators
    c_ops = [np.sqrt(g) * sigmaz()]

    # initial state
    psi0 = basis(2, 0)

    # times
    tlist = np.linspace(0, 5, 100)
    return H, c_ops, delta, g, psi0, tlist


