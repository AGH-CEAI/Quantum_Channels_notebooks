@app.cell
def _(delta, mesolve, np, psi0, sigmax, sigmay, sigmaz):
    # Angle
    theta = 0.2 * np.pi
    H_1 = delta * (np.cos(theta) * sigmaz() + np.sin(theta) * sigmax())
    # Hamiltonian
    tlist_1 = np.linspace(0, 5, 1000)
    # Obtain Time Evolution
    result = mesolve(H_1, psi0, tlist_1, [], [sigmax(), sigmay(), sigmaz()])
    return H_1, result, tlist_1


@app.cell(hide_code=True)
def _(mo):
    mo.md(r"""
    We can visualise the state on the Bloch sphere by using the `qutip.Bloch` class. We can add points to the Bloch sphere and also vectors representing states.
    """)
    return


@app.cell
def _(Bloch, np, psi0, result):
    # Extract expectation values for pauli matrices
    exp_sx_circ, exp_sy_circ, exp_sz_circ = result.expect
    exp_sx_circ, exp_sy_circ, exp_sz_circ = (np.array(exp_sx_circ), np.array(exp_sy_circ), np.array(exp_sz_circ))
    _sphere = Bloch()
    _sphere.add_points([exp_sx_circ, exp_sy_circ, exp_sz_circ], meth='l')
    _sphere.add_states(psi0)
    # Create Bloch sphere plot
    _sphere.render()
    _sphere.fig
    return exp_sx_circ, exp_sy_circ, exp_sz_circ


