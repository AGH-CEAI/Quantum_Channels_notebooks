@app.cell
def _(Bloch, H_1, mesolve, np, psi0, sigmax, sigmay, sigmaz, tlist_1):
    gamma_phase = 0.5
    c_ops_1 = [np.sqrt(gamma_phase) * sigmaz()]
    result_1 = mesolve(H_1, psi0, tlist_1, c_ops_1, [sigmax(), sigmay(), sigmaz()])
    exp_sx_dephase, exp_sy_dephase, exp_sz_dephase = result_1.expect
    exp_sx_dephase, exp_sy_dephase, exp_sz_dephase = (np.array(exp_sx_dephase), np.array(exp_sy_dephase), np.array(exp_sz_dephase))
    _sphere = Bloch()
    _sphere.add_points([exp_sx_dephase, exp_sy_dephase, exp_sz_dephase], meth='l')
    _sphere.add_states(psi0)
    _sphere.render()
    _sphere.fig
    return exp_sx_dephase, exp_sy_dephase, exp_sz_dephase


